def csv_columns(filename):
    # Открываем файл с указанным именем в кодировке UTF-8
    with open(filename, encoding='utf-8') as f:
        # Считываем всё содержимое файла, удаляем пробельные
        # символы по краям и разбиваем на строки по символу перевода строки
        lines = f.read().strip().split('\n')

    # Первая строка - заголовок, разбиваем её по запятой на названия столбцов
    headers = lines[0].split(',')

    # Создаём словарь с ключами - названиями столбцов,
    # и пустыми списками в качестве значений
    columns = {header: [] for header in headers}

    # Проходим по всем строкам, кроме первой (заголовка)
    for line in lines[1:]:
        # Разбиваем строку на значения по запятой
        values = line.split(',')
        # Для каждой пары (название столбца, значение) добавляем
        # значение в соответствующий список словаря
        for header, value in zip(headers, values):
            columns[header].append(value)

    # Возвращаем словарь, где ключ - название столбца, значение - список элементов этого столбца
    return columns
